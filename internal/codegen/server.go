func goFieldName(snake string) string { return snakeToCamel(snake) }

// goFieldType is a thin wrapper that keeps the historical call sites
// (composite-PK struct fields, HNSW search method queryVec parameter)
// stable while routing every type-mapping decision through coltype.
// NOT for proto field types — those come from buf-generated code.
func goFieldType(t dsl.FieldType, notNull bool) string {
	return coltype.GoType(t, notNull)
}

func vectorDistanceOp(ops dsl.VectorOps) string {
	switch ops {
	case dsl.VecOpsCosine:
		return "<=>"
	case dsl.VecOpsL2:
		return "<->"
	case dsl.VecOpsIP:
		return "<#>"
	}
	return "<=>"
}

// lowerFirst lower-cases the first rune of a string.
func lowerFirst(s string) string {
	if s == "" {
		return ""
	}
	if s[0] >= 'A' && s[0] <= 'Z' {
		return string(s[0]+'a'-'A') + s[1:]
	}
	return s
}
