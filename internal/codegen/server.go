// emitScanIntoProto writes scanInto<Entity>, scanInto<Entity>WithTotal,
// and scanInto<Entity>WithDistance. Each one scans a row in SELECT order
// (matching fieldColumns) into proto fields, using sql.NullX for nullable
// scalars and pgvector.Vector for vector columns.
func emitScanIntoProto(b *strings.Builder, e *dsl.Entity) {
	// Per-field scan locals + post-assign logic.
	var (
		localDecls  []string // "var emailLocal string"
		scanTargets []string // "&emailLocal"
		postAssigns []string // "out.Email = emailLocal"
	)
	for _, f := range e.Fields {
		local, target, assign := protoScanFragments(f)
		localDecls = append(localDecls, local)
		scanTargets = append(scanTargets, target)
		postAssigns = append(postAssigns, assign)
	}

	indent := "\t"
	indentedDecls := indent + strings.Join(localDecls, "\n"+indent)
	indentedAssigns := indent + strings.Join(postAssigns, "\n"+indent)

	fmt.Fprintf(b, `func scanInto%s(src interface{ Scan(dest ...any) error }, out *pb.%s) error {
%s
	if err := src.Scan(%s); err != nil {
		return err
	}
%s
	return nil
}

`,
		e.Name, e.Name,
		indentedDecls,
		strings.Join(scanTargets, ", "),
		indentedAssigns,
	)

	fmt.Fprintf(b, `func scanInto%sWithTotal(src interface{ Scan(dest ...any) error }, out *pb.%s, total *int64) error {
%s
	if err := src.Scan(%s, total); err != nil {
		return err
	}
%s
	return nil
}

`,
		e.Name, e.Name,
		indentedDecls,
		strings.Join(scanTargets, ", "),
		indentedAssigns,
	)

	fmt.Fprintf(b, `func scanInto%sWithDistance(src interface{ Scan(dest ...any) error }, out *pb.%s, distance *float32) error {
%s
	if err := src.Scan(%s, distance); err != nil {
		return err
	}
%s
	return nil
}

`,
		e.Name, e.Name,
		indentedDecls,
		strings.Join(scanTargets, ", "),
		indentedAssigns,
	)
}

// protoScanFragments returns the three lines the scan helper needs for
// one entity field: the local declaration, the rows.Scan target, and
// the post-scan assignment. Local name and protoField LHS follow the
// entity-server naming convention (`fooLocal` / `out.Foo`) so the
// emitted Go is readable next to the buf-generated struct.
func protoScanFragments(f dsl.Field) (decl, target, assign string) {
	local := lowerFirst(goFieldName(f.Name)) + "Local"
	protoField := "out." + goFieldName(f.Name)
	// Match the proto-emit rule: NOT NULL with declared DEFAULT is generated
	// as `optional`, so the proto field is a pointer. The scan layer must
	// produce a pointer assignment too, or the generated code fails to
	// compile with "cannot use <type> as *<type>".
	notNull := f.NotNull
	if f.Default != nil {
		notNull = false
	}
	return coltype.ScanFragments(f.Type, notNull, local, protoField)
}

// emitBindForInsert writes bindFor<Entity>Insert(in *pb.<Entity>) []any.
// Returns the positional bind args in the order of insertCols (which
// already excludes Identity/Serial columns).
func emitBindForInsert(b *strings.Builder, e *dsl.Entity, insertCols []string) {
	fmt.Fprintf(b, `func bindFor%sInsert(in *pb.%s) []any {
	return []any{
`, e.Name, e.Name)

	for _, colName := range insertCols {
		f := e.FindField(colName)
		if f == nil {
			fmt.Fprintf(b, "\t\tnil, // column %q not found on entity — codegen bug\n", colName)
			continue
		}
		fmt.Fprintf(b, "\t\t%s,\n", protoBindExpr(*f))
	}
	b.WriteString("\t}\n}\n\n")
}

// emitBindForUpdate writes bindFor<Entity>Update(in *pb.<Entity>, pk ...any) []any.
// The PK args come after the SET column args so the placeholder offsets
// in sqlUpdate<Entity> match. Variadic on the PK lets one signature
// serve both single-PK ($N) and composite-PK ($N, $N+1, …) entities;
// the caller splats a []any holding the PK columns in DSL declaration
// order, which is the same order the SQL emitter used when building the
// WHERE clause.
func emitBindForUpdate(b *strings.Builder, e *dsl.Entity, spec *pkSpec) {
	fmt.Fprintf(b, `func bindFor%sUpdate(in *pb.%s, pk ...any) []any {
	args := []any{
`, e.Name, e.Name)

	for _, f := range e.Fields {
		if spec.IsPKColumn(f.Name) || f.Identity || f.Serial {
			continue
		}
		fmt.Fprintf(b, "\t\t%s,\n", protoBindExpr(f))
	}
	b.WriteString("\t}\n\treturn append(args, pk...)\n}\n\n")
}

// protoBindExpr returns the Go expression that turns a proto field on
// `in` into the value pgx needs for bind, using the entity-server
// naming convention for getter and pointer references.
func protoBindExpr(f dsl.Field) string {
	protoGetter := "in.Get" + goFieldName(f.Name) + "()"
	protoFieldPtr := "in." + goFieldName(f.Name)
	// NOT NULL columns with a declared DEFAULT are emitted as `optional`
	// in the proto (see proto.go) so the caller can omit them and let
	// the INSERT's COALESCE fire the default. On the bind side that
	// means the proto field is a pointer; route through the same
	// Nullable helpers the nullable case uses so the bound value lands
	// as NULL (not the Go zero value) when the caller didn't set it.
	notNull := f.NotNull
	if f.Default != nil {
		notNull = false
	}
	return coltype.BindExpr(f.Type, notNull, protoGetter, protoFieldPtr)
}

// pkSpec describes the primary key of an entity for the server emitter. It
// hides the single-vs-composite split behind one set of accessor methods
// so every emit*Method can reach for the same vocabulary regardless of
// arity.
//
// Built once per entity at the top of emitGoServerEntity.
type pkSpec struct {
	// Composite is true when the PK has more than one column (declared via
	// `primary by col1, col2, ...` on the entity).
	Composite bool
	// Fields lists PK columns in DSL declaration order. CompositeID encoding
	// depends on this order — reorder = wire-shape change.
	Fields []*dsl.Field
	// EntityName is the user-facing entity name; the composite PK struct
	// type is `<EntityName>PK`.
	EntityName string
}

func buildPKSpec(e *dsl.Entity) (*pkSpec, error) {
	if len(e.CompositePK) > 0 {
		fields := make([]*dsl.Field, 0, len(e.CompositePK))
		for _, name := range e.CompositePK {
			f := e.FindField(name)
			if f == nil {
				return nil, fmt.Errorf("composite PK references unknown field %q", name)
			}
			fields = append(fields, f)
		}
		return &pkSpec{Composite: true, Fields: fields, EntityName: e.Name}, nil
	}
	pk := e.PrimaryField()
	if pk == nil {
		return nil, fmt.Errorf("no primary field")
	}
	return &pkSpec{Composite: false, Fields: []*dsl.Field{pk}, EntityName: e.Name}, nil
}

// IsPKColumn reports whether name is one of the PK columns. Used to skip
// PK fields in the UPDATE SET list.
func (p *pkSpec) IsPKColumn(name string) bool {
	for _, f := range p.Fields {
		if f.Name == name {
			return true
		}
	}
	return false
}

// ReturnType is the Go type used as Create's return + the type referenced
// in handler signatures. Single PK: the scalar Go type. Composite: the
// generated wrapper struct (`<Entity>PK`).
func (p *pkSpec) ReturnType() string {
	if p.Composite {
		return p.EntityName + "PK"
	}
	return goFieldType(p.Fields[0].Type, true)
}

// SigParam returns the parameter declaration as it appears in the method
// signature: `id int64` (single) or `pk AccountSessionPK` (composite).
func (p *pkSpec) SigParam() string {
	if p.Composite {
		return "pk " + p.EntityName + "PK"
	}
	return "id " + goFieldType(p.Fields[0].Type, true)
}

// CallArgs returns the comma-separated Go expressions that pass PK values
// into a SQL query.
func (p *pkSpec) CallArgs() string {
	if !p.Composite {
		return "id"
	}
	parts := make([]string, len(p.Fields))
	for i, f := range p.Fields {
		parts[i] = "pk." + goFieldName(f.Name)
	}
	return strings.Join(parts, ", ")
}

// WhereClause returns the SQL predicate that matches one row by PK,
// starting at placeholder $startIdx.
func (p *pkSpec) WhereClause(startIdx int) string {
	if !p.Composite {
		return fmt.Sprintf("%s = $%d", quoteIdent(p.Fields[0].Name), startIdx)
	}
	cols := make([]string, len(p.Fields))
	ph := make([]string, len(p.Fields))
	for i, f := range p.Fields {
		cols[i] = quoteIdent(f.Name)
		ph[i] = fmt.Sprintf("$%d", startIdx+i)
	}
	return "(" + strings.Join(cols, ", ") + ") = (" + strings.Join(ph, ", ") + ")"
}

// OrderByCols renders the ORDER BY column list.
func (p *pkSpec) OrderByCols() string {
	cols := make([]string, len(p.Fields))
	for i, f := range p.Fields {
		cols[i] = quoteIdent(f.Name)
	}
	return strings.Join(cols, ", ")
}

// ReturningCols mirrors OrderByCols.
func (p *pkSpec) ReturningCols() string { return p.OrderByCols() }

// CountPlaceholders returns the number of $N tokens the PK consumes.
func (p *pkSpec) CountPlaceholders() int { return len(p.Fields) }

