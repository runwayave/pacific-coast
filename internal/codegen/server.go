func emitProtoUpdateMethod(b *strings.Builder, e *dsl.Entity, srv string, spec *pkSpec) {
	// Update mirrors Delete's `[]any` PK pattern so the body is one
	// template across PK arities. The PK comes off the entity (`in`)
	// rather than a separate request shell — Update's wire shape carries
	// the full entity. CompositeID-encoded args feed both the SQL UPDATE
	// (where the PK predicate occupies the placeholder slots after the
	// SET columns; see sqlUpdate%s) and the post-commit cache /
	// outbox bookkeeping.
	//
	// SET list omits every PK column (see pkSpec.IsPKColumn). For
	// composite-PK + hypertable rows (e.g. vendor.Purchase, partitioned
	// on purchased_at), this means the partitioning column is already
	// excluded from SET, which is what TimescaleDB requires — attempting
	// to UPDATE a partitioning column raises a PG error.
	fmt.Fprintf(b, `// Update%s implements pb.%sServiceServer.
func (s *%s) Update%s(ctx context.Context, req *pb.Update%sRequest) (*pb.Update%sResponse, error) {
	ctx, cancel := runtime.Deadline(ctx, %sQueryTimeoutMS)
	defer cancel()

	in := req.GetEntity()
	if in == nil {
		return nil, fmt.Errorf("Update%s: entity is required")
	}
	id := []any{%s}

	tx, err := s.DB.BeginTx(ctx)
	if err != nil {
		return nil, err
	}
	defer func() { _ = tx.Rollback(context.Background()) }()

	args := bindFor%sUpdate(in, id...)
	tag, err := tx.Exec(ctx, sqlUpdate%s, args...)
	if err != nil {
		return nil, err
	}
	if tag.RowsAffected() == 0 {
		return nil, runtime.ErrNotFound
	}

	cacheID := runtime.CompositeID(id...)
	cur, _ := s.Cache.CurrentVersion(ctx, %q, cacheID)
	if err := s.Outbox.Enqueue(ctx, tx, %q, cacheID, cur+1); err != nil {
		return nil, err
	}
	if err := s.Outbox.EnqueueGenerationBump(ctx, tx, %q); err != nil {
		return nil, err
	}
	if err := tx.Commit(ctx); err != nil {
		return nil, err
	}

	out := &pb.%s{}
	if err := scanInto%s(s.DB.QueryRow(ctx, sqlGet%s, id...), out); err != nil {
		return nil, err
	}
	return &pb.Update%sResponse{Entity: out}, nil
}

`, e.Name, e.Name,
		srv, e.Name, e.Name, e.Name,
		lowerFirst(e.Name),
		e.Name,
		pkEntityAccess(spec),
		e.Name, e.Name,
		e.ID(), e.ID(), e.ID(),
		e.Name, e.Name, e.Name,
		e.Name)
}

