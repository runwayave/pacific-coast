// insertPlaceholdersFor builds the VALUES clause for sqlInsert<Entity>.
// For columns with a declared SQL DEFAULT, the placeholder is wrapped in
// COALESCE($N::<type>, <default-expr>) so that an unset proto field (which
// binds as NULL via the runtime.NullableX helpers) resolves to the column
// default at INSERT time instead of writing literal NULL. The cast is
// required because Postgres can't infer the parameter type when the
// placeholder is the first arg to COALESCE alongside a literal of a
// different shape.
//
// For columns without a default, the placeholder is the plain $N. Order
// matches insertCols so bind args still line up positionally.
func insertPlaceholdersFor(e *dsl.Entity, insertCols []string) string {
	parts := make([]string, len(insertCols))
	for i, name := range insertCols {
		f := e.FindField(name)
		ph := fmt.Sprintf("$%d", i+1)
		if f != nil && f.Default != nil {
			parts[i] = fmt.Sprintf("COALESCE(%s::%s, %s)", ph, sqlType(f.Type), defaultExpr(*f.Default))
			continue
		}
		parts[i] = ph
	}
	return strings.Join(parts, ", ")
}

// updateAssignments renders the "col1 = $1, col2 = $2, ..." body of UPDATE.
func updateAssignments(e *dsl.Entity, spec *pkSpec) string {
	parts := []string{}
	i := 0
	for _, f := range e.Fields {
		if spec.IsPKColumn(f.Name) {
			continue
		}
		if f.Identity || f.Serial {
			continue
		}
		i++
		parts = append(parts, fmt.Sprintf("%s = $%d", quoteIdent(f.Name), i))
	}
	if len(parts) == 0 {
		return ""
	}
	return strings.Join(parts, ", ")
}

